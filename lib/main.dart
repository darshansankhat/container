import 'dart:math';

import 'package:flutter/material.dart';

void main() {
  runApp(
    MaterialApp(
      debugShowCheckedModeBanner: false,
      home: Scaffold(
        body: Center(
          child: Container(
            width: 300,
            height: 300,
            color: Colors.red,
            alignment: Alignment.center,
            child: Container(
              width: 300,
              height: 300,
              decoration: BoxDecoration(
                shape: BoxShape.circle,
                color: Colors.yellow,
              ),
              alignment: Alignment.center,
              child: Transform.rotate(
                angle: pi / 4,
                child: Container(
                  width: 210,
                  height: 210,
                  decoration: BoxDecoration(
                      borderRadius: BorderRadius.only(
                          topLeft: Radius.circular(500),
                          bottomRight: Radius.circular(500)),
                      color: Colors.pink),
                  alignment: Alignment.center,
                  child: Transform.rotate(
                    angle: pi / 2,
                    child: Container(
                      width: 90,
                      height: 90,
                      decoration: BoxDecoration(
                          borderRadius: BorderRadius.only(
                            topLeft: Radius.circular(500),
                            bottomRight: Radius.circular(500),
                          ),
                          color: Colors.blue),
                      alignment: Alignment.center,
                      child: Container(
                        width: 55,
                        height: 55,
                        decoration: BoxDecoration(
                          shape: BoxShape.circle,
                          color: Colors.amber,
                        ),
                      ),
                    ),
                  ),
                ),
              ),
            ),
          ),
        ),
      ),
    ),
  );
}